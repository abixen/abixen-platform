package com.abixen.platform.core.interfaces.web.common;

import com.abixen.platform.common.model.enumtype.RoleType;
import lombok.extern.slf4j.Slf4j;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.RestController;

import java.util.List;

import static java.util.Arrays.asList;

@Slf4j
@RestController
@RequestMapping(value = "/api/dictionary/enum")
public class EnumController {

    @RequestMapping(value = "/roleType", method = RequestMethod.GET)
    List<RoleType> getRoles() {
        return asList(RoleType.class.getEnumConstants());
    }
}
